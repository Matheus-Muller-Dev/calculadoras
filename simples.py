number_1 = float(input('Digite o primeiro número: '))
number_2 = float(input('Digite o segunfo número: '))

# adição
print('{} + {} = '.format(number_1, number_2))
print(number_1 + number_2)
# subtração
print('{} - {} = '.format(number_1, number_2))
print(number_1 - number_2)
# multiplicação
print('{} * {} = '.format(number_1, number_2))
print(number_1 * number_2)
# Divisão
print('{} / {} = '.format(number_1, number_2))
print(number_1 / number_2)